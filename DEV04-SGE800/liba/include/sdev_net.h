#ifndef _SDEV_NET_H_
#define _SDEV_NET_H_

#include "typedef.h"
/*************************************************
  结构类型定义
*************************************************/
struct netfig{
	char ip[12];
	u16 port;
	u8 timeout;
	u8 index;
};

/******************************************************************************
*	函数:	sdev_net_setconfig
*	功能:	配置网络客户端功能
*	参数:
*	返回:	0				-	成功
			-ESYS			-	系统错误
			-ERR_INVAL		-	参数无效
			-ERR_NOINIT		-	没有初始化

*	说明:	无
******************************************************************************/
int sdev_net_setconfig(u8 cmd, void *cfg);

/******************************************************************************
*	函数:	sdev_net_getconfig
*	功能:	获取网络客户端功能属性
*	参数:
*	返回:	0				-	成功


*	说明:	无
******************************************************************************/
int sdev_net_getconfig(u8 cmd, void *cfg);

/******************************************************************************
*	函数:	sdev_net_send
*	功能:	网络客户端发送数据
*	参数:	buf				-	发送数据起始指针
			count			-	发送数据长度
*	返回:	>=0				-	发送成功的字节数
			-ESYS			-	系统错误
			-ERR_INVAL		-	参数无效
			-ERR_NOINIT		-	没有初始化

*	说明:	超时时间通过配置发送超时定
******************************************************************************/
int sdev_net_send(u8 *buf, u32 count);

/******************************************************************************
*	函数:	sdev_net_recv
*	功能:	网络客户端接收数据
*	参数:	buf				-	接收数据起始指针
			count			-	接收缓冲区长度
*	返回:	>=0				-	接收成功的字节数
			-ERR_SYS		-	错误
			-ERR_INVAL		-	参数无效
			-ERR_NOINIT		-	没有初始化

*	说明:
******************************************************************************/
int sdev_net_recv(u8 *buf, u32 count, u8 *type, u8 *tel);

/******************************************************************************
*	函数:	sdev_net_sendsmg
*	功能:	无
*	参数:
*	返回:

*	说明:	空函数
******************************************************************************/
int sdev_net_sendsmg(u8 *buf, u32 count, u8 *telnum, u8 tellen);

/******************************************************************************
*	函数:	sdev_net_recvsmg
*	功能:	无
*	参数:
*	返回:	0				-	成功(收到确认命令)
			-ERR_TIMEOUT	-	连接超时

*	说明:	空函数
******************************************************************************/
int sdev_net_recvsmg(u8 *buf, u32 count);

/******************************************************************************
*	函数:	sdev_net_connect
*	功能:
*	参数:	mode			-	连接方式：0创建新连接；1激活重连
*	返回:	0				-	成功(收到确认命令)
			-ERR_TIMEOUT	-	连接超时

*	说明:	无
******************************************************************************/
int sdev_net_connect(u8 mode);

/******************************************************************************
*	函数:	sdev_net_disconnect
*	功能:	无
*	参数:
*	返回:	0				-	成功(收到确认命令)
			-ERR_TIMEOUT	-	连接超时

*	说明:	无
******************************************************************************/
int sdev_net_disconnect(void);

/******************************************************************************
*	函数:	sdev_net_getstat
*	功能:	获得状态
*	参数:
*	返回:	0				-	成功


*	说明:	无
******************************************************************************/
int sdev_net_getstat(u8 cmd, void *stat);

/******************************************************************************
*	函数:	sdev_net_turnon
*	功能:	无
*	参数:
*	返回:	0				-	成功


*	说明:	无
******************************************************************************/
int sdev_net_turnon(void);

/******************************************************************************
*	函数:	sdev_net_turnoff
*	功能:	无
*	参数:
*	返回:

*	说明:	无
******************************************************************************/
int sdev_net_turnoff(void);

/******************************************************************************
*	函数:	sdev_net_poweron
*	功能:	上电
*	参数:
*	返回:	0				-	成功


*	说明:	无
******************************************************************************/
int sdev_net_poweron(void);

/******************************************************************************
*	函数:	sdev_net_poweroff
*	功能:	掉电
*	参数:
*	返回:	0				-	成功


*	说明:	无
******************************************************************************/
int sdev_net_poweroff(void);

/******************************************************************************
*	函数:	sdev_net_reset
*	功能:	无
*	参数:
*	返回:	0				-	成功


*	说明:	无
******************************************************************************/
int sdev_net_reset(void);

#endif /* SDEV_NET_H_ */
