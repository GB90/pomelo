/******************************************************************************
*	函数:	sdev_485_send
*	功能:	485口发送数据
*	参数:	buf				-	发送数据起始指针
			count			-	发送数据长度
*	返回:	>=0				-	发送成功的字节数
			-ESYS			-	系统错误
			-ERR_INVAL		-	参数无效
			-ERR_NOINIT		-	没有初始化

*	说明:	超时时间通过配置发送超时定
******************************************************************************/
int sdev_485_send(u8 *buf, u32 count)
{
	int ret;
	ret = comport_send (CFG_SDEV_485_COMPORT, buf, count);
	if (ret < 0){
		goto error;
	}
error:
	return(ret);
}

/******************************************************************************
*	函数:	sdev_485_recv
*	功能:	485口接收数据
*	参数:	buf				-	接收数据起始指针
			count			-	接收缓冲区长度
*	返回:	>=0				-	接收成功的字节数
			-ERR_SYS		-	错误
			-ERR_INVAL		-	参数无效
			-ERR_NOINIT		-	没有初始化

*	说明:
******************************************************************************/
int sdev_485_recv(u8 *buf, u32 count, u8 *type, u8 *tel)
{
	int ret;
	ret = comport_recv(CFG_SDEV_485_COMPORT, buf, count);
	if (ret < 0){
		goto error;
	}
error:
	return(ret);
}

