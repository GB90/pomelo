/******************************************************************************
*	函数:	sdev_485_setconfig
*	功能:	配置485口
*	参数:
*	返回:	0				-	成功
			-ESYS			-	系统错误
			-ERR_INVAL		-	参数无效
			-ERR_NOINIT		-	没有初始化

*	说明:	无
******************************************************************************/
int sdev_485_setconfig(u8 cmd, void *cfg)
{
	int ret;
	ret = comport_setconfig (CFG_SDEV_485_COMPORT, (comport_config_t *)cfg);
	if (ret){
		goto error;
	}
	ret = 0;
error:
	return(ret);
}

